public class Lec1Ex3 {
    public static void main(String[] args) {
        int binary = 0b1010;    // binary literal: 10
        int hex = 0x1F;
        char letter = 65;
        boolean isAvailable = false;

        System.out.println("Binary 0b1010: " + binary);
        System.out.println("Hex 0x1F: " + hex);
        System.out.println("Character from Unicode 65: " + letter);
        System.out.println("Boolean value: " + isAvailable);
    }
}
